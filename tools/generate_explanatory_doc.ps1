function Escape-XmlText([string]$text) {
  if ($null -eq $text) { return "" }
  return [System.Security.SecurityElement]::Escape($text)
}

function ParagraphXml([string]$text, [string]$style = "Normal", [switch]$PageBreakBefore) {
  $escaped = Escape-XmlText $text
  $pageBreak = ""
  if ($PageBreakBefore) {
    $pageBreak = '<w:pageBreakBefore/>'
  }
  return @"
<w:p>
  <w:pPr><w:pStyle w:val="$style"/>$pageBreak</w:pPr>
  <w:r><w:t xml:space="preserve">$escaped</w:t></w:r>
</w:p>
"@
}

function EmptyParagraphXml() {
  return "<w:p/>"
}

function CodeParagraphXml([string]$text) {
  $escaped = Escape-XmlText $text
  return @"
<w:p>
  <w:pPr><w:pStyle w:val="Code"/></w:pPr>
  <w:r><w:t xml:space="preserve">$escaped</w:t></w:r>
</w:p>
"@
}

function PageBreakXml() {
  return '<w:p><w:r><w:br w:type="page"/></w:r></w:p>'
}

